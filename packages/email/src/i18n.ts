export function getEmailTranslation(
  locale: string,
  key: string,
  params?: Record<string, string | number>
): string {
  const lang = (locale in translations ? locale : "tr") as EmailLocale;
  let text = translations[lang][key] ?? translations.tr[key] ?? key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }

  return text;
}

export function getStatusLabel(locale: string, status: string): string {
  return getEmailTranslation(locale, `status.${status}`);
}

export type { EmailLocale };
